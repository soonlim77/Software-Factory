using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace JustInTimeShippingCore
{
    [Serializable()]
    public class BoxInfo
    {
        public int Height { get; set; }
        public int Width { get; set; }
        public int Depth { get; set; }

        public BoxInfo()
        {
            
        }
        public BoxInfo(int h, int w, int d) : this()
        {
            Height = h;
            Width = w;
            Depth = d;

        }
    }
}
